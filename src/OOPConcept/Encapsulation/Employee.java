package OOPConcept.Encapsulation;

public class Employee {

	private int empName;
	private int empID;
	public int getEmpName() {
		return empName;
	}
	public void setEmpName(int empName) {
		this.empName = empName;
	}
	public int getEmpID() {
		return empID;
	}
	public void setEmpID(int empID) {
		this.empID = empID;
	}
	
	
	
}
